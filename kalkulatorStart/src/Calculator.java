public class Calculator {
    double addNumbers (double a, double b){
        double sum = a + b;
        return sum;
    }
    double subNumbers (double a, double b) {
        double roz = a - b;
        return roz;
    }
    double multiNumbers (double a, double b) {
        double mnoz = a * b;
        return mnoz;
    }
    double divNumbers (double a, double b) {
        double dziel = a / b;
        return dziel;
    }
}
